	/**
	 * set the colored cdev status info and enable/disable the reconnect
	 * button... depended on the cdevSystem.inConnected()
	 */
	public void setConnectButton() {
		if (cdevSystem.isConnected()) {
			synoptic.getResultPanel().statusText.setText("cdev connected");
			synoptic.getResultPanel().statusText.setBackground(Color.green);
			synoptic.getResultPanel().setConnectButtonLabel(DISCONNECT_STRING);
		} else {
			// cdev not connected
			synoptic.getResultPanel().statusText.setText("cdev not connected");
			synoptic.getResultPanel().statusText.setBackground(Color.red);
			synoptic.getResultPanel().setConnectButtonLabel(RECONNECT_STRING);
		}
	}

