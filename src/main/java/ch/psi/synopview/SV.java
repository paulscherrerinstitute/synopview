	/**
	 * reconnectCdev() is executed when the reconnect/disconnect Button is
	 * pressed in the ResultPanel
	 *
	 * Interface SVControlSystem
	 *
	 */
	public void sc_reconnectCdev() {
		if (cdevSystem.isConnected()) {
			// cdev is currently connected, so the "disconnect" buttun was
			// proberly clicked...
			cdevSystem.disconnect();
		} else {
			cdevSystem.sc_reconnectCdev();
		}
		// set the colored cdev status info and enable/disable the reconnect
		// button...
		setConnectButton();
	}



